def slugify(value: Optional[str]) -> str:
    """Conservative URL-safe slugifier.

    Lowercases, replaces any run of non-alphanum with "-", strips
    leading/trailing dashes. ASCII-only to keep URLs clean even for
    non-latin sources. Truncates at 80 chars.

    Returns "" when the input is falsy after normalization — the caller
    decides whether that's acceptable (we fall back to the occurrence
    date).
    """
    import re
    import unicodedata

    if not value:
        return ""
    # NFKD decompose + strip combining marks -> plain ASCII where possible
    normalized = unicodedata.normalize("NFKD", str(value))
    ascii_only = "".join(ch for ch in normalized if not unicodedata.combining(ch))
    lowered = ascii_only.lower()
    # Replace anything non-alphanum with "-"
    dashed = re.sub(r"[^a-z0-9]+", "-", lowered)
    # Collapse repeated dashes and strip
    clean = dashed.strip("-")
    return clean[:80]


async def generate_occurrence_slug(
    *,
    org_id: str,
    product_name: Optional[str],
    start_at: Optional[str],
    exclude_id: Optional[str] = None,
) -> str:
    """Build a unique slug for an occurrence, handling collisions.

    Composition: `slugify(product_name)-YYYY-MM-DD`. When the product
    name is empty the date alone is used. When two occurrences would
    collide (same product, same day), a short numeric suffix is appended
    (`-2`, `-3`, …) until the slug is free within the organization.

    `exclude_id` is respected during updates so an occurrence does not
    collide with itself.
    """
    base_name = slugify(product_name) if product_name else ""

    date_part = ""
    if start_at:
        try:
            # Accept ISO datetimes "2026-08-14T20:00:00" — keep the date.
            date_part = start_at[:10]
            # Light validation: must look like YYYY-MM-DD
            import re
            if not re.fullmatch(r"\d{4}-\d{2}-\d{2}", date_part):
                date_part = ""
        except Exception:
            date_part = ""

    parts = [p for p in (base_name, date_part) if p]
    base = "-".join(parts) or "evento"

    from database import event_occurrences_collection

    # Try the base first, then append -2, -3, … on collision.
    candidate = base
    suffix = 1
    while True:
        query = {"organization_id": org_id, "slug": candidate}
        if exclude_id:
            query["id"] = {"$ne": exclude_id}
        taken = await event_occurrences_collection.find_one(query, {"_id": 0, "id": 1})
        if not taken:
            return candidate
        suffix += 1
        candidate = f"{base}-{suffix}"
        if suffix > 50:  # defensive upper bound — should never hit in practice
            import secrets
            return f"{base}-{secrets.token_hex(3)}"


def build_map_url(occ: dict) -> Optional[str]:
    """Derive a Google Maps URL from an occurrence's location fields.

    Precedence:
      1. Explicit `map_url` field (admin override).
      2. lat/lng pair if both set.
      3. Composed address (venue + address + city + postal + country).
      4. Legacy `location` text.
      5. None — storefront omits the map link.

    The function is read-only; it never geocodes. Kept out of the
    Pydantic model to keep the schema side-effect free (dict in, str
    or None out). Called by the public catalog and the landing page
    endpoints when rendering event cards.
    """
    if not occ:
        return None

    explicit = occ.get("map_url")
    if explicit:
        return explicit

    lat, lng = occ.get("latitude"), occ.get("longitude")
    if lat is not None and lng is not None:
        return f"https://www.google.com/maps/search/?api=1&query={lat},{lng}"

    parts = [
        occ.get("venue_name"),
        occ.get("address"),
        occ.get("postal_code"),
        occ.get("city"),
        occ.get("country"),
    ]
    composed = ", ".join(str(p) for p in parts if p)
    if composed:
        from urllib.parse import quote_plus
        return f"https://www.google.com/maps/search/?api=1&query={quote_plus(composed)}"

    legacy = occ.get("location")
    if legacy:
        from urllib.parse import quote_plus
        return f"https://www.google.com/maps/search/?api=1&query={quote_plus(legacy)}"

    return None
