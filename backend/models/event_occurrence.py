"""
EventOccurrence — a dated instance of an event_ticket product.

Example:
  Product: "Cena in Masseria" (item_type=event_ticket)
  Occurrence A: 2026-08-14 at 20:30, capacity 40
  Occurrence B: 2026-08-21 at 20:30, capacity 40

Occurrences are org-scoped and linked to a product via product_id.
Only products with item_type="event_ticket" may have occurrences.

E2 — structured event details:
  The pre-E2 schema carried only a single `location` text (max 255) and
  terse `notes`. E2 adds structured fields so the storefront can render
  a proper event page (venue + address + map pin + hero image + long
  description in markdown). EVERY new field is optional — a pre-E2
  occurrence continues to serialize and display correctly; the public
  catalog falls back to `location` text when structured fields are
  absent.
"""

from pydantic import BaseModel, Field, ConfigDict, field_validator
from typing import Optional
from datetime import datetime
from .common import generate_id, utc_now

OCCURRENCE_STATUSES = ("draft", "published", "closed", "cancelled")


class EventOccurrenceBase(BaseModel):
    product_id: str                                # FK → products._id (must be event_ticket)
    start_at: str                                  # ISO datetime: "2026-08-14T20:30:00"
    end_at: Optional[str] = None                   # ISO datetime (optional)
    location: Optional[str] = Field(default=None, max_length=255)
    capacity: Optional[int] = Field(default=None, ge=1)
    price_override: Optional[float] = Field(default=None, ge=0)  # overrides product.unit_price
    notes: Optional[str] = Field(default=None, max_length=1000)

    # ── E2: structured location ──────────────────────────────────────────
    # All optional. When the admin fills them, the storefront renders a
    # proper venue card + map link. `location` text remains a free-form
    # fallback (e.g. "Online event — link inviato dopo l'acquisto").
    venue_name: Optional[str] = Field(default=None, max_length=150)
    address: Optional[str] = Field(default=None, max_length=255)
    city: Optional[str] = Field(default=None, max_length=100)
    postal_code: Optional[str] = Field(default=None, max_length=20)
    country: Optional[str] = Field(default=None, max_length=2)  # ISO-3166 alpha-2
    latitude: Optional[float] = Field(default=None, ge=-90, le=90)
    longitude: Optional[float] = Field(default=None, ge=-180, le=180)
    # Optional explicit map URL override. When absent, the storefront
    # builds a link from lat/lng or from the full address (see helper
    # `build_map_url`). Never geocoded server-side — manual paste only.
    map_url: Optional[str] = Field(default=None, max_length=500)

    # ── E2: presentation ─────────────────────────────────────────────────
    # Hero image for the landing page (E3). Square / wide ratios both
    # supported by the frontend; full URL or relative to /uploads.
    cover_image_url: Optional[str] = Field(default=None, max_length=500)
    # Long markdown description. Renders on the landing page. Keep under
    # 5000 chars — Michele writes the story of the evening, not a novel.
    long_description: Optional[str] = Field(default=None, max_length=5000)
    # Optional external reference (EventBrite id, Ticketone code, ecc.)
    # purely informative — never dereferenced server-side.
    external_ref_id: Optional[str] = Field(default=None, max_length=100)

    # E3: URL slug for the public landing page. Optional — autogenerated
    # at create time from product name + date when missing. Enforced
    # unique per organization by a sparse index in database.py. Admin
    # can override manually from the occurrence editor.
    slug: Optional[str] = Field(default=None, max_length=160)

    # G6: archive flag. Archived events disappear from the default
    # /events listing so the admin isn't cluttered by past editions,
    # but remain reachable via the "Archivio" filter and by direct URL.
    # Never set automatically — the merchant chooses to archive.
    is_archived: bool = False

    @field_validator("country", mode="before")
    @classmethod
    def _normalize_country(cls, v):
        """Accept either uppercase or lowercase; store uppercase ISO-2."""
        if v is None or v == "":
            return None
        if not isinstance(v, str):
            return v
        code = v.strip().upper()
        return code or None


